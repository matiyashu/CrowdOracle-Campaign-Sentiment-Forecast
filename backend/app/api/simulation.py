# Interview prompt optimization prefix.
# This prefix instructs the Agent to reply in plain text without invoking tools.
INTERVIEW_PROMPT_PREFIX = "Drawing on your persona, all prior memories, and past actions, reply to me in plain text without calling any tools: "


def optimize_interview_prompt(prompt: str) -> str:
    """
    Optimize an interview prompt by prepending a prefix that discourages tool use.

    Args:
        prompt: original question

    Returns:
        Optimized prompt
    """
    if not prompt:
        return prompt
    # Avoid double-prepending the prefix
    if prompt.startswith(INTERVIEW_PROMPT_PREFIX):
        return prompt
    return f"{INTERVIEW_PROMPT_PREFIX}{prompt}"


